    private void BT_AbrirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BT_AbrirMouseClicked
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser("C:\\ARCHIVOS\\");
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) { // ABRIÃ“ EL ARCHIVO
            // ABRIR EL ARCHIVO
            InputStream in;
            InputStreamReader isReader = null;
            BufferedReader bufferedReader = null;

            try {
                Path path = fileChooser.getSelectedFile().toPath();
                in = Files.newInputStream(path);
                isReader = new InputStreamReader(in);
                bufferedReader = new BufferedReader(isReader);

                TXT_Block_Notas.read(bufferedReader, "ABRIR EL ARCHIVO");
            } catch (IOException ex) {
                System.out.println(ex.getMessage());

            } finally {
                try {
                    bufferedReader.close();
                    isReader.close();
                } catch (IOException ex) {
                    Logger.getLogger(BLOCK_NOTAS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_BT_AbrirMouseClicked

    private void BT_GuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BT_GuardarMouseClicked
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser("C:\\ARCHIVOS\\");
        int result = fileChooser.showSaveDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            OutputStream out;
            OutputStreamWriter osWriter = null;
            BufferedWriter bufferedWriter = null;

            try {
                Path path = fileChooser.getSelectedFile().toPath();
                out = Files.newOutputStream(path);
                osWriter = new OutputStreamWriter(out);
                bufferedWriter = new BufferedWriter(osWriter);

                TXT_Block_Notas.write(bufferedWriter);
            } catch (IOException ex) {
                System.out.println(ex.getMessage());

            } finally {
                try {
             bufferedWriter.close();
             osWriter.close();
                } catch (IOException ex){
                   Logger.getLogger(BLOCK_NOTAS.class.getName()).log(Level.SEVERE, null, ex);  
                }
            }
        }

    }//GEN-LAST:event_BT_GuardarMouseClicked

